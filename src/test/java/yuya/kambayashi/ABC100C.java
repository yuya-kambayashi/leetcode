    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        int[] aa = new int[n];
        for (int i = 0; i < n; i++) {
            aa[i] = sc.nextInt();
        }

        int ans = 0;

        for (int i = 0; i < n; i++) {
            int a = aa[i];

            if (a % 2 == 1) {
                continue;
            }

            int t = 1;
            while (t < 31) {
                if (a % (int) Math.pow(2, t) == 0 && a % (int) Math.pow(2, t + 1) != 0) {
                    ans += t;
                    break;
                }
                t++;
            }

        }

        System.out.println(ans);
    }

