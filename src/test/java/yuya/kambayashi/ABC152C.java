    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        int[] pp = new int[n];
        for (int i = 0; i < n; i++) {
            pp[i] = sc.nextInt();
        }

        int beforeMin = pp[0];
        int ans = 1;
        for (int i = 1; i < n; i++) {

            if (pp[i] <= beforeMin) {
                beforeMin = pp[i];
                ans++;
            }

        }

        System.out.println(ans);
    }
