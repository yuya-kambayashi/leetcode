    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();

        List<Point2D> points = new ArrayList<>();
        for (int i = 0; i < n; i++) {
            points.add(new Point2D.Double((double) sc.nextInt(), (double) sc.nextInt()));
        }

        for (int i = 0; i < n; i++) {
            for (int j = i + 1; j < n; j++) {
                for (int k = j + 1; k < n; k++) {

                    var p1 = points.get(i);
                    var p2 = points.get(j);
                    var p3 = points.get(k);

                    double t = ((p2.getX() - p1.getX()) * (p3.getY() - p1.getY()) - (p3.getX() - p1.getX()) * (p2.getY() - p1.getY()));
                    if (Math.abs(t) < 0.001) {
                        System.out.println("Yes");
                        return;
                    }
                }
            }
        }

        System.out.println("No");
    }
