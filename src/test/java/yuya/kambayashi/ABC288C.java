    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        final int m = sc.nextInt();
        List<List<Integer>> gg = new ArrayList<>();
        for (int i = 0; i < n; i++) {
            gg.add(new ArrayList<Integer>());
        }
        for (int i = 0; i < m; i++) {
            int a = sc.nextInt();
            int b = sc.nextInt();
            a--;
            b--;
            gg.get(a).add(b);
            gg.get(b).add(a);
        }

        Queue<Integer> que = new LinkedList<>();

        boolean[] used = new boolean[n];

        int s = 0;
        for (int i = 0; i < n; i++) {
            if (!used[i]) {
                s++;
                que.add(i);

                while (!que.isEmpty()) {

                    int t = que.poll();

                    for (var p : gg.get(t)) {
                        if (!used[p]) {
                            used[p] = true;
                            que.add(p);
                        }
                    }

                }
            }
        }
        System.out.println(m - n + s);
    }
