    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        long[] aa = new long[n];
        long sum = 0;
        for (int i = 0; i < n; i++) {
            aa[i] = sc.nextLong();
            sum += aa[i];
        }

        long min = sum / n;
        long max = min + 1;

        long minus = 0, plus = 0;
        for (int i = 0; i < n; i++) {
            long a = aa[i];
            if (a < min) {
                minus += min - a;
            } else if (max < a) {
                plus += a - max;
            }
        }

        System.out.println(Math.max(minus, plus));

    }
