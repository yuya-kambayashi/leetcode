    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        final String s = sc.next();

        if (n % 2 == 0) {
            System.out.println("No");
            return;
        }

        int p = (n + 1) / 2;
        p--;

        for (int i = 0; i <= p - 1; i++) {
            if (s.charAt(i) != '1') {
                System.out.println("No");
                return;
            }
        }
        if (s.charAt(p) != '/') {
            System.out.println("No");
            return;
        }
        for (int i = p + 1; i < n; i++) {
            if (s.charAt(i) != '2') {
                System.out.println("No");
                return;
            }
        }
        System.out.println("Yes");
    }
