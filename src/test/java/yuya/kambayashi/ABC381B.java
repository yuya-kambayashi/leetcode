    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final String s = sc.next();
        int t = s.length();

        for (int i = 1; i <= t / 2; i++) {

            if (s.charAt((2 * i - 1) - 1) != s.charAt((2 * i) - 1)) {
                System.out.println("No");
                return;
            }
        }
        int[] cnt = new int[26];
        for (int i = 0; i < t; i++) {
            int c = s.charAt(i);
            cnt[c - 'a']++;
        }
        for (int i = 0; i < 26; i++) {
            if (cnt[i] != 0 && cnt[i] != 2) {
                System.out.println("No");
                return;
            }
        }

        System.out.println("Yes");
    }
