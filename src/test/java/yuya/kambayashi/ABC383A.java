    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int n = sc.nextInt();
        int a = 0;
        int before = 0;
        for (int i = 0; i < n; i++) {
            int t = sc.nextInt();

            int at = a - (t - before);
            if (at <= 0) {
                a = 0;
            } else {
                a = at;
            }
            before = t;

            int v = sc.nextInt();
            a += v;
        }

        System.out.println(a);
    }
