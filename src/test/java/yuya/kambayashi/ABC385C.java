    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int n = sc.nextInt();
        int[] height = new int[n];
        for (int i = 0; i < n; i++) {
            height[i] = sc.nextInt();
        }
        int max = 1;

        for (int i = 0; i < n; i++) {
            int h = height[i];

            for (int j = 1; j < n; j++) {
                int count = 1;
                int current = i;

                while (true) {
                    current += j;
                    if (current >= n || height[current] != h) {
                        break;
                    }
                    count++;
                }
                max = Math.max(max, count);
            }
        }
        System.out.println(max);
    }
