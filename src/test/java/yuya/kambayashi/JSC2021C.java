    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        final int a = sc.nextInt();
        final int b = sc.nextInt();

        long max = 0;
        for (int i = 1; i <= b - 1; i++) {
            int multia = 1;
            while (true) {
                int ta = i * multia;
                if (ta < a) {
                    multia++;
                    continue;
                }

                int tb = i * (multia + 1);
                if (tb <= b) {
                    max = Math.max(max, i);
                } else {
                    break;
                }

                multia++;
            }
        }

        System.out.println(max);
    }
